package io.zeta.metaspace.model.usergroup.result;


import io.zeta.metaspace.model.usergroup.DBInfo;

import java.util.List;

public class NotAllotDatabaseSearchResult {
    private String sourceId;
    private String sourceName;
    private String sourceType;
    private String ip;
    private String description;
    private List<DBInfo> dbInfoList;

    public String getSourceId() {
        return sourceId;
    }

    public void setSourceId(String sourceId) {
        this.sourceId = sourceId;
    }

    public String getSourceName() {
        return sourceName;
    }

    public void setSourceName(String sourceName) {
        this.sourceName = sourceName;
    }

    public String getSourceType() {
        return sourceType;
    }

    public void setSourceType(String sourceType) {
        this.sourceType = sourceType;
    }

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public List<DBInfo> getDbInfoList() {
        return dbInfoList;
    }

    public void setDbInfoList(List<DBInfo> dbInfoList) {
        this.dbInfoList = dbInfoList;
    }
}
